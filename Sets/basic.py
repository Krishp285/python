# A set is a collection of unordered, unindexed, unique elements in Python.

# Defined using curly braces { } or the set() constructor.

# Sets are mutable (we can add or remove elements), but the elements inside a set must be immutable (like numbers, strings, tuples).

# 2. Key Features of Sets

# Unordered → No fixed order of elements.

# No duplicates → Duplicate elements are automatically removed.

# Heterogeneous elements → Can contain different data types.

# Mutable → We can add or remove elements.

# Immutable elements only → Elements must be hashable (no lists or dicts inside sets).



