# What is String
# A string is a sequence of characters enclosed in quotes. In Python, you can use single quotes (' '), double quotes (" "), or triple quotes (''' ''' or """ """) to define a string.

# Example:
string1 = 'Hello, World!'
string2 = "Python is fun!"
string3 = '''This is a multi-line string. 
It can span multiple lines.'''

